    private void pesquisarDadosRelatorio(String tipoOs) {
        String sql = "select * from ktcaos where tipoos=?";
        try {

            pst = conexao.prepareStatement(sql);
            pst.setString(1, tipoOs);
            rs = pst.executeQuery();

            Object columnNames[] = {"OS", "DATA", "EQUIPAMENTO", "VALOR"};
            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(columnNames);

            int qtdOS = 0;

            while (rs.next()) {

                // CRIA VARIÁVEIS
                var os = (rs.getString(1));
                Date data = (rs.getDate(2));
                var equipamento = (rs.getString(3));
                var valor = (rs.getString(7));
               
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                String dataFormatada = formato.format(data);

                // SETA DADOS NO MODELO DA TABLE
                model.addRow(new Object[]{os, dataFormatada, equipamento, valor});

                // SETA DADOS NA TABLE
                jTableRelatorio.setModel(model);

                qtdOS = qtdOS + 1;
                labelTotal.setText("Quantidade de Itens: " + Integer.toString(qtdOS));

            }

          

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não conectou no banco: " + e);
        }
    }

